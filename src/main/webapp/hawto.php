	<h1>How to...</h1>
	<p>All of this information you can also find in the ReadMe.txt file of Your JETRIS.<br></p>
	
	<h2>Install</h2>
	<p><u>To start JETRIS try one of the following options: </u></p>
	<ul>
		<li>Double click on the JAR File to start JETRIS. If this didn't work, then you didn't associate your JAR Files with your JRE. </li>
		<li>Double click on JETRIS.bat for Windows users or on JETRIS for Linux users.</li>
		<li>Open the console go to your JETRIS folder and type: <br>java -jar JETRIS.jar</li>
	</ul>
	<br>
	<p><u>For example:</u></p>
	<p>You have a Windows Operating System, your JETRIS folder is C:\JETRIS then:<br>
	Open Start Menu -> Run type "cmd" in the console type "cd C:\JETRIS" hit Enter then type "java -jar JETRIS.jar" hit Enter</p>
	<div class="rule"></div>
	
	<h2>Play</h2>
	<p><u>Use the following keys to play JETRIS:</u></p>
	<ul>
		<li>A or Left Arrow - Move the figure to left</li>
		<li>D or Right Arrow - Move the figure to right</li>
		<li>W or Up Arrow - Rotate the figure</li>
		<li>S or Down Arrow - Move the figure quick down</li>
		<li>Space - Drop the figure immediately</li>
		<li>P or 'Pause' Button - Pause</li>
		<li>R or 'Restart' Button - Restart</li>
	</ul>
	<br>
	<div class="rule"></div>
	<h2>Save your old HiScores after version update</h2>
	<p>Just copy the old JETRIS.dat file to your new version of jetris folder. </p>
